def is_palindrome(text):
    audit_text = []
    for i in text.lower():
        if i.isalpha():
            audit_text.append(i)

    if audit_text[:] == audit_text[::-1]:
        return True
    else:
        return False

txt = input()

print(is_palindrome(txt))

