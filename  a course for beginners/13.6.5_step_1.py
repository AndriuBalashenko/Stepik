from math import pow, sqrt
def solve(a, b, c):
    a, b, c = float(a), float(b), float(c)
    d = pow(b, 2) - (4 * a * c)
    if d == 0:
        x = -b / (2 * a)
        return x, x

    if d > 0:
        x_1 = (- b + sqrt(pow(b, 2) - 4 * a * c)) / (2 * a)
        x_2 = (- b - sqrt(pow(b, 2) - 4 * a * c)) / (2 * a)
        x_3 = min(x_1, x_2)
        x_4 = max(x_1, x_2)
        return x_3, x_4


