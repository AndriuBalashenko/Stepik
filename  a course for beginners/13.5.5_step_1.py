def is_password_good(password):

    if len(password) < 8:
        return  False
    flag_A = False
    flag_a = False
    flag_1 = False
    for item in password:
        if item.isupper():
            flag_A = True
        elif item.islower():
            flag_a = True
        elif item.isdigit():
            flag_1 = True
    return flag_A and flag_a and flag_1


