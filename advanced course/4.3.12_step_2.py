row = input().split()

res_list = []
temp_list = [row[0]]
for i in range(1, len(row)):
    if row[i - 1] == row[i]:
        temp_list.append(row[i])
    else:
        res_list.append(temp_list)
        temp_list = []
        temp_list.append(row[i])
res_list.append(temp_list)

