# –ú–∞–≥–∏—á–µ—Å–∫–∏–π –∫–≤–∞–¥—Ä–∞—Ç üå∂Ô∏è.
# –ú–∞–≥–∏—á–µ—Å–∫–∏–º –∫–≤–∞–¥—Ä–∞—Ç–æ–º –ø–æ—Ä—è–¥–∫–∞ n –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è –∫–≤–∞–¥—Ä–∞—Ç–Ω–∞—è —Ç–∞–±–ª–∏—Ü–∞ —Ä–∞–∑–º–µ—Ä–∞ n√ón, —Å–æ—Å—Ç–∞–≤–ª–µ–Ω–Ω–∞—è –∏–∑ –≤—Å–µ—Ö —á–∏—Å–µ–ª 1,2,3,‚Ä¶,n**2
#  —Ç–∞–∫, —á—Ç–æ —Å—É–º–º—ã –ø–æ –∫–∞–∂–¥–æ–º—É —Å—Ç–æ–ª–±—Ü—É, –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–µ –∏ –∫–∞–∂–¥–æ–π –∏–∑ –¥–≤—É—Ö –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π —Ä–∞–≤–Ω—ã –º–µ–∂–¥—É —Å–æ–±–æ–π.
# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –∑–∞–¥–∞–Ω–Ω–∞—è –∫–≤–∞–¥—Ä–∞—Ç–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ –º–∞–≥–∏—á–µ—Å–∫–∏–º –∫–≤–∞–¥—Ä–∞—Ç–æ–º.
#
# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ,
# –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã: n —Å—Ç—Ä–æ–∫, –ø–æ n —á–∏—Å–µ–ª –≤ –∫–∞–∂–¥–æ–π, —Ä–∞–∑–¥–µ–ª—ë–Ω–Ω—ã–µ –ø—Ä–æ–±–µ–ª–∞–º–∏.
#
# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å–ª–æ–≤–æ YES, –µ—Å–ª–∏ –º–∞—Ç—Ä–∏—Ü–∞ —è–≤–ª—è–µ—Ç—Å—è –º–∞–≥–∏—á–µ—Å–∫–∏–º –∫–≤–∞–¥—Ä–∞—Ç–æ–º, –∏ —Å–ª–æ–≤–æ NO –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

from math import pow

n = int(input())
matrix = [[int(i) for i in input().split()] for _ in range(n)]  # —Å–æ–∑–¥–∞—ë–º –º–∞—Ç—Ä–∏—Ü—É
m_const = int(n * (pow(n, 2) + 1) / 2)  # –ú–∞–≥–∏—á–µ—Å–∫–∞—è –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∞, –ø–µ—Ä–µ–≤–æ–¥–∏–º float –≤ int.
flag = 'YES'

total_sum = sum([matrix[i][i] for i in range(n)])  # i = j, –æ—Ç—Å—é–¥–∞ [i][i] - –≤—ã—á–∏—Å–ª—è–µ–º —Å—É–º–º—É –≤—Å–µ—Ö –∑–Ω–∞—á–µ–Ω–∏–π –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
if total_sum != m_const:  # –∏ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å –º–∞–≥–∏—á–µ—Å–∫–æ–π –∫–æ–Ω—Å—Ç–∞–Ω—Ç–æ–π.
    flag = 'NO'

total_sum = sum([matrix[i][n - i - 1] for i in range(n)])  # –≤—ã—á–∏—Å–ª—è–µ–º —Å—É–º–º—É –≤—Å–µ—Ö –∑–Ω–∞—á–µ–Ω–∏–π –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
if total_sum != m_const:  # –∏ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å –º–∞–≥–∏—á–µ—Å–∫–æ–π –∫–æ–Ω—Å—Ç–∞–Ω—Ç–æ–π.
    flag = 'NO'

for i in range(n):  # —Å—á–∏—Ç—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫–∏,
    total_sum = sum(matrix[i])  # —Å—á–∏—Ç–∞–µ–º —Å—É–º–º—É –∫–∞–∂–¥–æ–π
    if total_sum != m_const:  # –∏ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å –º–∞–≥–∏—á–µ—Å–∫–æ–π –∫–æ–Ω—Å—Ç–∞–Ω—Ç–æ–π.
        flag = 'NO'

for j in range(n):  # —Å—á–∏—Ç—ã–≤–∞–µ–º —Å—Ç–æ–ª–±—Ü—ã, —Å—á–∏—Ç–∞–µ–º —Å—É–º–º—É –∫–∞–∂–¥–æ–≥–æ –∏ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º
    total_sum = sum([matrix[i][j] for i in range(n)])
    if total_sum != m_const:  # —Å –º–∞–≥–∏—á–µ—Å–∫–æ–π –∫–æ–Ω—Å—Ç–∞–Ω—Ç–æ–π.
        flag = 'NO'

checklist = list(range(1, n**2 + 1)) # –ø—Ä–æ–≤–µ—Ä–æ—á–Ω—ã–π —Å–ø–∏—Å–æ–∫ –¥–ª—è —É—Å–ª–æ–≤–∏—è "—Å–æ—Å—Ç–∞–≤–ª–µ–Ω–Ω–∞—è –∏–∑ –≤—Å–µ—Ö —á–∏—Å–µ–ª 1, 2, 3,‚Ä¶,n**2"
new_list = []   # –∏–∑ –º–∞—Ç—Ä–∏—Ü—ã —Å–æ–∑–¥–∞–¥–∏–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ —Å –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–º–∏ –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏
for r in range(n):
    for c in range(n):
        new_list.append(matrix[r][c])
if sorted(new_list) != checklist: # –∏ —Å—Ä–∞–≤–Ω–∏–º –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ —Å –ø—Ä–æ–≤–µ—Ä–æ—á–Ω—ã–º —Å–ø–∏—Å–∫–æ–º.
    flag = 'NO'   # –ï—Å–ª–∏ –µ—Å—Ç—å —Ä–∞–∑–Ω–∏—Ü–∞, —Ç–æ –º–µ–Ω—è–µ–º —Ñ–ª–∞–≥.

print(flag)
