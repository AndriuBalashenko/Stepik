import string
row = input().lower().split()
new_list = [word.strip(string.punctuation) for word in row]

res_dict = {}
for elem in new_list:
    res_dict[elem] = res_dict.get(elem, 0) + 1

min_result = min(res_dict.values())

res_lst = []
for key, value in res_dict.items():
    if value == min_result:
        res_lst.append(key)

print(min(res_lst))

