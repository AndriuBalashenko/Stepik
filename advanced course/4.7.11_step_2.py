# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∏—Ä—É—é—â—É—é –º–∞—Ç—Ä–∏—Ü—É, —Ä–∞–∑–¥–µ–ª—è—è —ç–ª–µ–º–µ–Ω—Ç—ã —Å–∏–º–≤–æ–ª–æ–º –ø—Ä–æ–±–µ–ª–∞.

n = int(input())
matrix = [[int(i) for i in input().split()] for i in range(n)]
m = int(input())
doublet = [[i for i in row] for row in matrix]
result = [[0] * n for i in range(n)]
for _ in range(m - 1):
    result = [[0] * n for i in range(n)]
    for i in range(n):
        for j in range(n):
            for r in range(n):
                result[i][j] += matrix[i][r] * doublet[r][j]
    doublet = result
for r in range(n):
    for c in range(n):
        print(result[r][c], end=' ')
    print()
