# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —É–∫–∞–∑–∞–Ω–Ω—ã–π –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫.
#
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ù–µ –∑–∞–±—É–¥—å—Ç–µ –≤—ã–∑–≤–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é chunked(), —á—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç üòÄ.


def chanked(row, n):

    res_list = []
    temp_list = []
    for i in row:
        temp_list.append(i)
        if len(temp_list) == n:
            res_list.append(temp_list)
            temp_list = []
    if len(row) < n:
        res_list = [row]
    elif len(row) % n != 0:
        res_list.extend([[row[-1]]])
    return res_list

row = input().split()
n = int(input())
print(chanked(row, n))


