def pascal(n):

    list_1 = []
    for i in range(0, n + 1):
        temp_list = []
        for j in range(i + 1):
            if j == 0 or j == i:
                temp_list.append(1)
            else:
                temp_list.append(list_1[i - 1][j - 1] + list_1[i - 1][j])

        list_1.append(temp_list)
    return list_1[n]

n = int(input())
print(pascal(n))


